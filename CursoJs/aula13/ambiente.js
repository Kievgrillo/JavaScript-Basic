//fazendo um teste com estrutura de repeticao while
var c = 1;
while (c <= 6) {
  console.log('Tudo bem');
  c++;
}

//fazendo um teste com estrutura de repeticao DO while
var c = 1;
do {
  console.log('Tudo bem');
  c++;
} while (c <= 6);

//fazendo um teste de estrutura de repeticao FOR
console.log('Vai comecar');
for (var c = 1; c <= 6; c++) {
  console.log(c);
}
console.log('fim');
